package com.java.day18.Oct_29_2023;

public class WrapperClass_Demo {

	// int - Integer
	// btye - Byte
	// short - Short
	// long - Long
	// double - Double
	// boolean - Boolean
	// float - Float
	// char - Character

	// What are Wrapper classes?
	// Pre-defined classes which can contain primitive data type. They wrap
	// primitive data to Wrapper Class Object.

	public static void main(String[] args) {
		int a = 10; //this is a simple variable
		System.out.println("the primitive datat type value is : " + a);

		Integer b = 100; //this is an Integer Wrapper Class Object
		System.out.println("the Wrapper Class Object value : " + b);
		
		// what is the difference between a and b?
	}
}
