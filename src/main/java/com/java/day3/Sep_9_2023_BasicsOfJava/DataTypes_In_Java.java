package com.java.day3.Sep_9_2023_BasicsOfJava;

public class DataTypes_In_Java {

	public static void main(String[] args) {

		// Data Types = int, byte, short, long, double, boolean, float, char

		// Integer
		// I want to store a small integer value
		// Integer has a range of -2^31 to 2^31 - 1
		// Integer range = -2147483648 to 2147483647
		int i = 200;
		int j = -300;

		// Byte
		// Range of -128 to 127
		// least used data type, smallest
		byte b1 = -128;
		byte b2 = 127;

		// short -32768 to 32767
		short s1 = -32768;
		short s2 = 32767;

		// Long
		// biggest data type
		// Range -
		long l1 = -9223372036854775808L;
		long l2 = 9223372036854775807L;

		// Double
		// decimal value
		double k = 21.34;

		// Boolean
		boolean b3 = true;
		boolean b4 = false;

		boolean b5 = 1 < 2;
		boolean b6 = 2 > 3;
		
		//char 
		//single value
		char c = '1';
		char c1 = 'S';

	}
}
